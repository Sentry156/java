package com.itheima.demo1.literal;

public class LiteralDemo {
    public static void main(String[] args) {
        printLiteral();
    }
    public static void printLiteral(){
        System.out.println(1); //整形
        System.out.println(1.0); //浮点型
        System.out.println(true); //布尔型
        System.out.println(false);
        System.out.println('a'); //字符型 有且仅有一个字符
        System.out.println("hello"); // 字符串
        System.out.println("hello\nworld"); //换行符
        System.out.println("hello\tworld"); // 进制符




    }
}
